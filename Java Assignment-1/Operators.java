public class Operators {
    public static void main(String[] args) {
        int a = 52, b = 9;

        // Arithmetic
        System.out.println("Sum: " + (a + b));
        System.out.println("Difference: " + (a - b));

        // Relational
        System.out.println("a > b: " + (a > b));

        // Logical
        System.out.println("a > 0 && b > 0: " + (a > 0 && b > 0));
    }
}